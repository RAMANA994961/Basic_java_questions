package java1;
import java.util.Scanner;
public class count_numbers_DM {
	public static void main(String[] args) {
		System.out.println("enter number");
		Scanner sc = new Scanner(System.in);
		int a = sc.nextInt();
		int count=0;
		while(a>=0) {
			a/=0;
			count++;
		}
		System.out.println(count);
	}

}
